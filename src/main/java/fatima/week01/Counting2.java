package fatima.week01;

public class Counting2 {
    public static void main(String[] args) {
        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
        }

        System.out.println("==============");

        int j = 0;
        while (j <= 10){
            System.out.println(j);

            ++j;
        }

    }
}
