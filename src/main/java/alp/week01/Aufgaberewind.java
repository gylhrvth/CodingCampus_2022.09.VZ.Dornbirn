package alp.week01;

public class Aufgaberewind {

    public static void main(String[] args) {
        System.out.println("Aufgabe rewind:");
        for (int i = 10; i >=-10; i--) {
            if (i < 10){
                System.out.print(", ");
            }
            System.out.print(i);
        }
        System.out.println();


    }
}
